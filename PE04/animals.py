animals = ["cow", "pig", "horse", "goat", "duck", "goose", "chicken"]   #defines a list with seven strings

print(f"The first three items in the list are: {animals[0:3]}")         #prints a slice of the  list from index 0 to 2

print(f"Three items from  the middle of the  list are: {animals[2:5]}") #prints a slice of the  list from index 2 to 4

print(f"The last three items in the list are: {animals[4:7]}")          #prints a slice of the list from index 4 to 6